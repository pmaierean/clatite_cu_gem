package org.maiereni.imaging.common.stl.vo;

/**
 * @author Petre Maierean
 *
 */
public class Hexahedron {
	private float x0, y0, z0, x1, y1, z1;

	public float getX0() {
		return x0;
	}

	public void setX0(float x0) {
		this.x0 = x0;
	}

	public float getY0() {
		return y0;
	}

	public void setY0(float y0) {
		this.y0 = y0;
	}

	public float getZ0() {
		return z0;
	}

	public void setZ0(float z0) {
		this.z0 = z0;
	}

	public float getX1() {
		return x1;
	}

	public void setX1(float x1) {
		this.x1 = x1;
	}

	public float getY1() {
		return y1;
	}

	public void setY1(float y1) {
		this.y1 = y1;
	}

	public float getZ1() {
		return z1;
	}

	public void setZ1(float z1) {
		this.z1 = z1;
	}
	
}
